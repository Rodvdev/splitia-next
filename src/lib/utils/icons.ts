// Get all icons for a category
export function getIconsByCategory(categoryId: string, type: IconType): IconItem[] {
  if (type === 'emoji') {
    return emojiIcons[categoryId] || [];
  } else {
    return lucideIcons[categoryId] || [];
  }
}

// Get all categories for a type
export function getCategoriesByType(type: IconType): IconCategory[] {
  if (type === 'emoji') {
    return emojiCategories;
  } else {
    return lucideCategories;
  }
}

// Get icon by value (for finding selected icon)
export function findIconByValue(value: string | LucideIcon, type: IconType): IconItem | undefined {
  const categories = type === 'emoji' ? emojiCategories : lucideCategories;
  
  for (const category of categories) {
    const icons = getIconsByCategory(category.id, type);
    const found = icons.find(icon => {
      if (type === 'emoji') {
        return icon.value === value;
      } else {
        // For lucide icons, compare by name since components can't be compared directly
        return typeof icon.value !== 'string' && typeof value !== 'string' && 
               (icon.value as any).displayName === (value as any).displayName;
      }
    });
    if (found) return found;
  }
  return undefined;
}

